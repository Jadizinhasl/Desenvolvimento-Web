var numero1 = Number(prompt('Digite o primeiro numero: '))
var numero2 = Number(prompt('Digite o segundo numero: '))

var soma = numero1 + numero2
var subtracao = numero1 - numero2
var multiplicacao = numero1 * numero2
var potenciacao = numero1 ** numero2
var divisao = numero1 / numero2

alert("O resultado da soma é: " + soma)
alert("O resultado da subtração é: " + subtracao)
alert('O resultado da multiplicação é: ' + multiplicacao)
alert('O resultado da potenciacao é: ' + potenciacao)
alert('O resultado da divisão é: ' + divisao)